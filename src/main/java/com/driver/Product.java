package com.driver;

public class Product {
    public int product(int x, int y) {
        int a = x + y;
        return a;
    }
    public int product(int x,int y,int z){
        int sum=x+y+z;
        return sum;
    }
    public double product(double x,double y){
        double c=x+y;
        return c;
    }
}
